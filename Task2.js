/***

Subtask-1:

Find all the odd numbers from 61 to 100.

 */


let i = 61;

for (i = 61; i <= 100; i++) {
  if (i % 2 === 1) {
    console.log('All ODD numbers from 61 to 100 : ' + i);
  }
}



